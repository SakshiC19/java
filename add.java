// Java Program to implement
// Direct Addition to Add two Numbers

class add {

    public static int sum(int num1, int num2) {
        return num1 + num2;
    }

    // main function
    public static void main(String[] args) {
        add ob = new add();
        int res = ob.sum(28, 49);
        System.out.println(res);
    }
}
